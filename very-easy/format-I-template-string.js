const format = (a, b, c) => {
  const template = `Their names were: ${a}, ${b} and ${c}.`;
  return template;
};

console.log(format("John", "Joe", "Jack"));
console.log(format("Peter", "Pin", "Pan"));
console.log(format("E", "Da", "Bit"));
console.log(format("Bulbasaur", "Charmander", "Squirtle"));
