const intToString = (num) => String(num);

const stringToInt = (num) => parseInt(num);

console.log(intToString(4));
console.log(intToString(65));
console.log(intToString(29348));
console.log(intToString(49583908545));
console.log(stringToInt("4"));
console.log(stringToInt("65"));
console.log(stringToInt("29348"));
console.log(stringToInt("49583908545"));
