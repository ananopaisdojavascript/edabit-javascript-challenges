const detectWord = (str) => {
  // Temos aqui nossa cadeia de caracteres com letras maiúsculas e algumas letras minúsculas escondidas (str)
  // Usar o método .split() para converter uma cadeia de caracteres em um vetor
  const arr = str.split("");
  // Criar um vetor para colcoar as letras maiúsculas
  const hiddenWord = [];
  // Usar o método forEach (percorre todos os elementos de um vetor e aplica uma função a cada um desses elementos) para percorrer o vetor e colcoar as letras minúsculas em um novo vetor
  const findTheLowerCase = arr.forEach((item) => {
    // Usar o método .toLowerCase() para identificar as letras maiúsculas e colocá-las no vetor
    if (item === item.toLowerCase()) return hiddenWord.push(item);
  });
  // Usar o método .join() para converter o vetor em uma cadeia de caracteres
  const lowerCaseWord = hiddenWord.join("");
  return lowerCaseWord;
};

